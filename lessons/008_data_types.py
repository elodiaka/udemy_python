# Strings
first_name = "John"
print ("# Strings")
print (first_name)

# Numbers
age = 35
print ("# Numbers")
print (age)

# Lists (=arrays, editable, starts with 0)
team_members = ["John", "Bob", "Tina"]
print ("# Lists")
print (team_members[2]) # print by index
print (team_members) # print whole list

# Tuples (=lists, not editable)
team_members = ("John", "Bob", "Tina")
print ("# Tuples")
print (team_members[1]) # print by index
print (team_members) # print whole list

# Dictionaries (key: value)
fav_pizza ={
	"John": "Pepperoni",
	"Bob": "Cheese",
	"Tina": "Supreme"
}

print ("# Dictionaries")
print (fav_pizza["John"]) #print value by key